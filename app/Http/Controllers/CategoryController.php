    public function showTotalFood() {
        $categories = Category::all()
            ->map(function ($category) {
            return [
                'id' => $category['id'],
                'name' => $category['name'],
                'image' => $category['image'],
            ];
            })
            ->toArray();

        return view('category.totalfood', compact('categories'));
    }

